from __future__ import annotations
"""
Движок скриптов продаж.
Генерирует персонализированные скрипты на основе методологий:
Белфорт (уверенность), Рэкхэм SPIN (вопросы), Восс ФБР (эмпатия),
Гитомер (доверие), Манн (российская специфика).
"""


def _niche_type(niche: str) -> str:
    n = (niche or "").lower()
    if any(x in n for x in ["маникюр","педикюр","салон","парикмахер","брови","ресниц","косметол","массаж","эпиляц","тату"]):
        return "beauty"
    if any(x in n for x in ["автосервис","шиномонтаж","автозапч","эвакуатор","детейлинг","автостекл"]):
        return "auto"
    if any(x in n for x in ["доставка еды","кейтеринг","торты","кафе","ресторан"]):
        return "food"
    if any(x in n for x in ["фитнес","тренажер","тренер","йога","пилатес"]):
        return "fitness"
    if any(x in n for x in ["бухгалтер","юрист","реклама","печать","полиграф","дизайн"]):
        return "b2b"
    if any(x in n for x in ["стоматол","ветерин","клиник","медицин"]):
        return "medical"
    if any(x in n for x in ["репетитор","детский центр","автошкол","курс"]):
        return "edu"
    if any(x in n for x in ["гостиниц","хостел","турагент","туры"]):
        return "travel"
    return "service"  # сантехник, электрик, ремонт и т.д.


