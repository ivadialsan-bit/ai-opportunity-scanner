def generate_scripts(
    name: str,
    niche: str,
    city: str,
    website: str,
    phone: str,
    email: str,
    flags: str,
    product: str,
    price: str,
    demo_url: str = "http://5.129.200.18:8080/demo-48h/",
) -> dict[str, str]:
    """
    Генерирует полный набор скриптов для лида.
    Возвращает словарь с ключами:
      call_full, call_short, call_sec,
      msg_wa_full, msg_wa_short,
      msg_email_subject, msg_email_body,
      kp_text,
      obj_no_need, obj_expensive, obj_think, obj_have_someone, obj_not_season
    """
    t = _niche_type(niche)
    pain, loss, competitor = _pain_by_type(t, niche)
    n = name or "ваша компания"
    ni = niche or "услуги"
    c = city or "вашем городе"
    p = price or "4 900 ₽"
    has_site = bool((website or "").strip())
    test_price = "2 900 ₽"

    # ── СКРИПТ ЗВОНКА — ПОЛНЫЙ (Белфорт + SPIN) ──
    if has_site:
        spin_q = f"Как сейчас клиенты записываются к вам — через сайт или в основном звонят?"
        spin_pain = f"А бывает что заявки теряются — звонят когда вы заняты, или пишут в нерабочее время?"
        spin_money = f"Если в неделю 2–3 таких случая — сколько это выходит в деньгах за месяц? [пауза]"
        offer = f"Я могу усилить то что уже есть: добавить онлайн-запись прямо с сайта и автоответ в Telegram. Клиент нажимает кнопку — вы получаете заявку мгновенно, в любое время."
    else:
        spin_q = f"Скажите — как сейчас к вам обращаются клиенты, звонят или в основном через 2ГИС находят?"
        spin_pain = f"А бывает что не дозваниваются или пишут поздно вечером когда уже не отвечаете?"
        spin_money = f"Если 2–3 таких случая в неделю — по вашему среднему чеку это сколько выходит в месяц? [пауза, дать посчитать]"
        offer = f"Именно это я и закрываю. Страница заявки — клиент находит вас в картах, нажимает кнопку, заполняет форму — вы получаете уведомление в Telegram моментально, в любое время суток."

    call_full = f"""Добрый день. Меня зовут [ваше имя], занимаюсь цифровой упаковкой для {ni} в {c}.

Нашёл «{n}» — вижу одну конкретную вещь которую можно закрыть за 48 часов. Вам удобно две минуты?

[да / молчание → продолжай]

{spin_q}

[слушаешь, не перебиваешь]

{spin_pain}

[слушаешь]

{spin_money}

Понятно. Значит часть заявок точно уходит мимо. {pain.capitalize()}.

{offer}

За 48 часов. Тест — {test_price}. Если за 30 дней ни одной заявки — верну деньги.

Хотите покажу пример прямо сейчас? Могу скинуть в WhatsApp — займёт 10 секунд."""

    # ── КОРОТКИЙ ЗВОНОК (Манн — 30 секунд) ──
    call_short = f"""Здравствуйте. Я по поводу «{n}».

Нашёл вас в {c} — {pain}.

Делаю страницу заявки за 48 часов, тест {test_price}. Если не понравится — не платите.

Хотите покажу пример?"""

    # ── ЕСЛИ СЕКРЕТАРЬ ──
    call_sec = f"""Здравствуйте! Соедините с руководителем, пожалуйста.

[если спросят по какому вопросу]

По вопросу онлайн-заявок от клиентов. Это коротко, две минуты."""

